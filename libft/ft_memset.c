#include "libft.h"

void	*ft_memset(void *t, int b, size_t n)
{
	char		*f;
	size_t		c;

	f = (char *)t;
	c = 0;
	while (c < n)
	{
		f[c] = b;
		c++;
	}
	return (f);
}
